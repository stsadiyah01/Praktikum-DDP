# Penjumlahan
def penjumlahan(bil1,bil2):
    hasil = bil1 + bil2
    print("Hasil Penjumlahan:",hasil)

# Pengurangan
def pengurangan(bil1,bil2):
    hasil = bil1 - bil2
    print("Hasil Pengurangan:",hasil)

# Perkalian
def perkalian(bil1,bil2):
    hasil = bil1 * bil2
    print("Hasil Perkalian:",hasil)

# Pembagian
def pembagian(bil1,bil2):
    hasil = bil1 / bil2
    print("Hasil Pembagian:",hasil)

# Eksponen
def perpangkatan(bil1,bil2):
    hasil = bil1 ** bil2
    print("Hasil Pangkat:",hasil)
    
# Modulus
def modulus(bil1,bil2):
    hasil = bil1 % bil2
    print("Hasil Sisa Bagi:",hasil)

# Floor Division
def floor_division(bil1,bil2):
    hasil = bil1 // bil2
    print("Hasil bulat pembagiannya adalah",hasil)

# Logaritma
import math
def log(bil) :
    print("log dari:",bil,"yaitu", math.log(bil))  

# Sin
def sin(bil):
    print("sin dari:",bil,"yaitu", math.sin(bil)) 

# Akar
def akar(bil):
    print("akar dari:",bil,"yaitu", math.sqrt(bil))  

# Cos
def cos(bil):
    print("cos dari:",bil,"yaitu", math.cos(bil))  



    

    

